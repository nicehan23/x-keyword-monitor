"""
트윗 생성용 프롬프트 템플릿
- 정보 전달형 공부법 트윗
- 콴다 자연스러운 언급 조절
"""


def get_tweet_prompt(trends_text: str, previous_tweets_text: str, include_qanda: bool) -> str:
    """트윗 생성 프롬프트 반환"""

    qanda_instruction = ""
    if include_qanda:
        qanda_instruction = """
7. 이번 트윗에는 '콴다'를 자연스럽게 한 번 언급해줘
   - 예: "콴다 같은 AI 풀이 앱 활용하면 더 효율적" 정도의 톤
   - 절대 직접적 광고가 아닌, 팁의 일부로 녹여낼 것"""
    else:
        qanda_instruction = """
7. 이번 트윗에서는 콴다나 특정 앱을 언급하지 마세요"""

    previous_section = ""
    if previous_tweets_text:
        previous_section = f"""
[최근에 이미 생성한 트윗들 — 이것들과 겹치지 않게]
{previous_tweets_text}
"""

    return f"""당신은 학생들에게 실용적인 공부법을 알려주는 교육 콘텐츠 X(트위터) 계정을 운영하고 있습니다.

[최근 X에서 반응이 좋았던 공부법/교육 관련 트윗들]
{trends_text}

{previous_section}

[메인 트윗 작성 규칙]
1. 공부법 주제를 흥미롭게 던지는 훅(hook) 역할
2. 마지막에 반드시 "어떻게 하냐면... ⇣⇣" 을 붙여서 스레드로 유도
3. 한글 기준 100자 이내 (스레드 유도 문구 포함)
4. 이모지 1~2개 활용 가능
5. 해시태그 1~2개 포함 (#공부법 #수능 #시험 #공부팁 등에서 선택)
6. 위 트렌드에서 영감을 받되, 절대 그대로 복사하지 말 것
7. 자연스러운 한국어 구어체 (딱딱한 문어체 X)
{qanda_instruction}

[좋은 메인 트윗 예시]
- "시험 2주 전, 이 루틴만 따라하면 성적 오름 📈 어떻게 하냐면... ⇣⇣ #공부법"
- "수학 틀린 문제 다시 봐도 이해 안 될 때 💡 어떻게 하냐면... ⇣⇣ #공부팁"
- "집중력 5분도 안 가는 사람? 이 방법 써봐 어떻게 하냐면... ⇣⇣ #공부법"

[출력]
메인 트윗 본문만 출력하세요. 따옴표나 설명 없이 바로 트윗 텍스트만."""


def get_thread_prompt(main_tweet: str, num_replies: int, include_qanda: bool) -> str:
    """메인 트윗에 이어지는 스레드(답글) 생성 프롬프트"""

    qanda_thread = ""
    if include_qanda:
        qanda_thread = f"""
- 마지막 답글에서 "콴다 같은 AI 풀이 앱 활용하면 더 효율적" 정도로 자연스럽게 언급
- 절대 직접적 광고가 아닌, 팁의 마무리로 녹여낼 것"""
    else:
        qanda_thread = """
- 콴다나 특정 앱을 언급하지 마세요"""

    return f"""당신은 학생들에게 실용적인 공부법을 알려주는 X(트위터) 계정을 운영하고 있습니다.

아래 메인 트윗에 이어지는 스레드(답글)를 {num_replies}개 작성해주세요.

[메인 트윗]
{main_tweet}

[스레드 작성 규칙]
1. 각 답글은 한글 기준 130자 이내
2. 메인 트윗이 던진 주제를 구체적으로 풀어서 설명
3. 각 답글은 하나의 세부 팁/단계를 다룸 (넘버링 활용)
4. 실용적이고 바로 실천 가능한 내용
5. 마지막 답글은 요약 또는 응원 메시지로 마무리
6. 자연스러운 한국어 구어체
{qanda_thread}

[좋은 스레드 예시]
메인: "시험 2주 전, 이 루틴만 따라하면 성적 오름 📈 어떻게 하냐면... ⇣⇣ #공부법"
답글1: "1️⃣ 1주차: 오답노트 싹 다 복습. 틀린 문제만 모아서 왜 틀렸는지 한 줄 정리. 이게 제일 효율 좋음"
답글2: "2️⃣ 기출 3회독. 처음엔 풀고, 두 번째는 시간 재고, 세 번째는 안 보고 풀기. 패턴이 보이기 시작함"
답글3: "3️⃣ 2주차: 취약 단원만 집중 공격. 다 잘하려고 하지 말고 약한 부분만 잡아도 점수 확 오름"
답글4: "이거 실천만 해도 상위권 가능. 화이팅 🔥"

[출력 형식]
답글을 ---로 구분해서 출력하세요. 번호나 "답글1:" 같은 접두어 없이 본문만.
---
첫 번째 답글 내용
---
두 번째 답글 내용
---
세 번째 답글 내용"""


