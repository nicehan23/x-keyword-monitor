"""
트윗 생성용 프롬프트 템플릿
- 정보 전달형 공부법 트윗
- 콴다 자연스러운 언급 조절
- Top 트윗 스타일/말투 분석 반영
"""

import random

THREAD_HOOKS = [
    "어떻게 하냐면... ⇣⇣",
    "-천천히 자세하게 풀어볼게요-",
    "타래로 하나씩 알려드릴게요",
]


def get_random_hook() -> str:
    return random.choice(THREAD_HOOKS)


def get_tweet_prompt(trends_text: str, previous_tweets_text: str, include_qanda: bool) -> str:
    """트윗 생성 프롬프트 반환"""

    hook = get_random_hook()

    qanda_instruction = ""
    if include_qanda:
        qanda_instruction = """
8. 이번 트윗에는 '콴다'를 자연스럽게 한 번 언급해줘
   - 예: "콴다 같은 AI 풀이 앱 활용하면 더 효율적" 정도의 톤
   - 절대 직접적 광고가 아닌, 팁의 일부로 녹여낼 것"""
    else:
        qanda_instruction = """
8. 이번 트윗에서는 콴다나 특정 앱을 언급하지 마세요"""

    previous_section = ""
    if previous_tweets_text:
        previous_section = f"""
[최근에 이미 생성한 트윗들 — 이것들과 겹치지 않게]
{previous_tweets_text}
"""

    return f"""당신은 학생들에게 실용적인 공부법을 알려주는 교육 콘텐츠 X(트위터) 계정을 운영하고 있습니다.

[최근 X에서 반응이 좋았던 공부법/교육 관련 트윗들]
{trends_text}

{previous_section}

[⭐ 스타일 분석 — 반드시 먼저 수행]
위 인기 트윗들의 Top 5(반응이 가장 높은 5개)를 분석하세요:
- 문장 구조: 질문형인지, 단정형인지, 나열형인지
- 말투: 반말/존댓말, 친근한 톤/전문적 톤, 감탄사 사용 여부
- 이모지 사용 패턴: 어디에 몇 개를 쓰는지
- 훅(hook) 방식: 공감형("~한 적 있지?"), 도발형("이거 모르면 손해"), 정보형("~하는 법")
- 글 길이와 리듬: 짧고 끊어 쓰는지, 한 문장으로 쭉 이어가는지

이 분석 결과를 바탕으로 아래 트윗을 작성하되, Top 5 트윗의 스타일을 자연스럽게 흡수하세요.

[메인 트윗 작성 규칙]
1. 공부법 주제를 흥미롭게 던지는 훅(hook) 역할
2. 마지막에 반드시 "{hook}" 을 붙여서 스레드로 유도
3. 한글 기준 100자 이내 (스레드 유도 문구 포함)
4. 위 Top 5 트윗의 이모지 사용 패턴을 참고 (과하지 않게)
5. 해시태그 1~2개 포함 (#공부법 #수능 #시험 #공부팁 등에서 선택)
6. 위 트렌드에서 영감을 받되, 절대 그대로 복사하지 말 것
7. Top 5 트윗의 말투와 문장 구조를 자연스럽게 따라할 것
{qanda_instruction}

[출력]
메인 트윗 본문만 출력하세요. 스타일 분석 내용은 출력하지 마세요. 따옴표나 설명 없이 바로 트윗 텍스트만."""


