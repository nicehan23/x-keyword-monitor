"""
트윗 생성용 프롬프트 템플릿
- 정보 전달형 공부법 트윗
- Top 5 스타일 분석 반영
- 메인 트윗 훅 문구 3종 랜덤
- 특정 앱/서비스 언급 금지
- 이모지 사용 금지 (넘버링 ➊➋➌➍➎만 예외)
"""
import random


# ──────────────────────────────────────────────
# 메인 트윗 마지막에 붙는 훅 문구 (랜덤 선택)
# ──────────────────────────────────────────────
THREAD_HOOKS = [
    "어떻게 하냐면... ⇣⇣",
    "-천천히 자세하게 풀어볼게요-",
    "타래로 하나씩 알려드릴게요",
]


def get_random_hook() -> str:
    """스레드 유도 훅 문구를 랜덤으로 반환"""
    return random.choice(THREAD_HOOKS)


def get_tweet_prompt(trends_text: str, previous_tweets_text: str, include_qanda: bool = False) -> str:
    """트윗 생성 프롬프트 반환 (Top 5 스타일 분석 포함)"""

    hook = get_random_hook()

    previous_section = ""
    if previous_tweets_text:
        previous_section = f"""
[최근에 이미 생성한 트윗들 — 이것들과 겹치지 않게]
{previous_tweets_text}
"""

    return f"""당신은 학생들에게 실용적인 공부법을 알려주는 교육 콘텐츠 X(트위터) 계정을 운영하고 있습니다.

[최근 X에서 반응이 좋았던 공부법/교육 관련 트윗들]
{trends_text}

{previous_section}

[스타일 분석 — 중요]
위 트렌드 트윗들을 먼저 분석한 뒤 트윗을 작성하세요:
- 문장 구조: 짧은 문장 vs 긴 문장, 끊어 쓰기 패턴
- 말투/톤: 반말 vs 존댓말, 친근한 톤 vs 정보 전달 톤
- 훅 스타일: 질문형, 단정형, 도발형 중 어떤 패턴이 잘 먹히는지
- 리듬감: 한 줄의 길이, 줄바꿈 타이밍
위 분석 결과를 반영하여 트렌드에 맞는 스타일로 작성하세요.

[메인 트윗 작성 규칙]
1. 공부법 주제를 흥미롭게 던지는 훅(hook) 역할
2. 마지막에 반드시 "{hook}" 을 붙여서 스레드로 유도
3. 한글 기준 100자 이내 (스레드 유도 문구 포함)
4. 이모지 절대 사용 금지 (넘버링 ➊➋➌➍➎만 예외)
5. 해시태그 1~2개 포함 (#공부법 #수능 #시험 #공부팁 등에서 선택)
6. 위 트렌드에서 영감을 받되, 절대 그대로 복사하지 말 것
7. 자연스러운 한국어 구어체 (딱딱한 문어체 X)
8. 특정 앱이나 서비스를 절대 언급하지 말 것 — 순수 공부법 콘텐츠만

[좋은 메인 트윗 예시]
- "시험 2주 전, 이 루틴만 따라하면 성적 오름 {hook} #공부법"
- "수학 틀린 문제 다시 봐도 이해 안 될 때 {hook} #공부팁"
- "집중력 5분도 안 가는 사람? 이 방법 써봐 {hook} #공부법"

[출력]
메인 트윗 본문만 출력하세요. 따옴표나 설명 없이 바로 트윗 텍스트만."""


def get_thread_prompt(main_tweet: str, num_replies: int, include_qanda: bool = False) -> str:
    """메인 트윗에 이어지는 스레드(답글) 생성 프롬프트"""

    return f"""당신은 학생들에게 실용적인 공부법을 알려주는 X(트위터) 계정을 운영하고 있습니다.

아래 메인 트윗에 이어지는 스레드(답글)를 {num_replies}개 작성해주세요.

[메인 트윗]
{main_tweet}

[스레드 작성 규칙]
1. 각 답글은 한글 기준 130자 이내
2. 메인 트윗이 던진 주제를 구체적으로 풀어서 설명
3. 각 답글은 하나의 세부 팁/단계를 다룸 (넘버링은 ➊➋➌➍➎ 사용)
4. 실용적이고 바로 실천 가능한 내용
5. 메인 트윗의 말투와 톤을 스레드에서도 일관되게 유지
6. 자연스러운 한국어 구어체
7. 마지막 답글도 구체적인 팁/방법으로 작성 (응원 문구나 '화이팅' 같은 마무리 금지)
8. 특정 앱이나 서비스를 절대 언급하지 말 것 — 순수 공부법 콘텐츠만
9. 이모지 절대 사용 금지 (넘버링 ➊➋➌➍➎만 예외)

[좋은 스레드 예시]
메인: "시험 2주 전, 이 루틴만 따라하면 성적 오름 어떻게 하냐면... ⇣⇣ #공부법"
답글1: "➊ 1주차: 오답노트 싹 다 복습. 틀린 문제만 모아서 왜 틀렸는지 한 줄 정리. 이게 제일 효율 좋음"
답글2: "➋ 기출 3회독. 처음엔 풀고, 두 번째는 시간 재고, 세 번째는 안 보고 풀기. 패턴이 보이기 시작함"
답글3: "➌ 2주차: 취약 단원만 집중 공격. 다 잘하려고 하지 말고 약한 부분만 잡아도 점수 확 오름"
답글4: "➍ 자기 전 10분 그날 공부한 핵심 키워드만 훑기. 수면 중 장기기억으로 넘어가는 효과 있음"

[출력 형식]
답글을 ---로 구분해서 출력하세요. 번호나 "답글1:" 같은 접두어 없이 본문만.
---
첫 번째 답글 내용
---
두 번째 답글 내용
---
세 번째 답글 내용"""


